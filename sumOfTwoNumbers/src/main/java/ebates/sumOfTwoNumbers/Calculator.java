package ebates.sumOfTwoNumbers;

/**
 * sum of two numbers class
 * 
 * @author steegala
 *
 */
public class Calculator {

	/**
	 * methods take two integers and returns sum of two integers.
	 * 
	 * @param firstNumber, first integer
	 * @param secondNumber, second integer
	 * @return sum of two integers
	 */
	public static Integer sumOfTwoNumbers(Integer firstNumber, Integer secondNumber) {
		return firstNumber + secondNumber;

	}

}
